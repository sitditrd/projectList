package java0821_basic;
/*
 * 컴파일 : 원본소스 -> 기계어
 * javac라는 명령어로 클래스명을 써주면 된다. "여기서는 java001_main.java"이다. 
 * eclipse에서는 저장하면 javac가 실행되는 것이다.
 * 
 * 컴파일을 한후에 class파일이 생성이 되고 -> java001_main.class
 * 
 * 실행하면 된다. (아래와 같은 과정으로)
 * 
 * java java001_main으로 실행을 한다.
 *
 * 윈도우 실행 단축키 ctrl+F11
 * tab의 단축키 ctrl+space
 */

public class Java001_main {
	// 현재 클래스를 실행시키기 위해서 가장 먼저 호출되는 메소드이다.

	public static void main(String[] args) {
		System.out.println("java test");
	} // end main : 메인이 끝나는 지점

} // end class : 클래스가 끝나는 지점